
class Calculadora{
    constructor(){}
    //Funcao que soma 2 numeros quaisquer
    soma(numero1,numero2){
        return numero1 + numero2
    }
    //Funcao que subtrai 2 numeros quaisquer
    subtracao(numero1,numero2){
        return numero2 - numero1
    }
    //Funcao que multiplica 2 numeros quaisquer
    multiplicacao(numero1, numero2){
        return numero1 * numero2
    }
    //Funcao que divide 2 numeros quaisquer
    divisao(numero1, numero2){
        return numero2 / numero1
    }

}


module.exports = Calculadora